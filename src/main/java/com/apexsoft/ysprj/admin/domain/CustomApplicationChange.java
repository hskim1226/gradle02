public class CustomApplicationChange extends ApplicationChange {


    private String korName;
    private String applId;
    private String collName;
    private String campName;
    private String deptName;
    private String majName;
    private String rgstNo;
    private String telNum;
    private String applChgCodeName;
    private String chgStsCodeName;


    public String getKorName() {
        return korName;
    }

    public void setKorName(String korName) {
        this.korName = korName;
    }

    public String getApplId() {
        return applId;
    }

    public void setApplId(String applId) {
        this.applId = applId;
    }

    public String getCollName() {
        return collName;
    }

    public void setCollName(String collName) {
        this.collName = collName;
    }

    public String getCampName() {
        return campName;
    }

    public void setCampName(String campName) {
        this.campName = campName;
    }

    public String getDeptName() {
        return deptName;
    }

    public void setDeptName(String deptName) {
        this.deptName = deptName;
    }

    public String getMajName() {
        return majName;
    }

    public void setMajName(String majName) {
        this.majName = majName;
    }

    public String getRgstNo() {
        return rgstNo;
    }

    public void setRgstNo(String rgstNo) {
        this.rgstNo = rgstNo;
    }

    public String getTelNum() {
        return telNum;
    }

    public void setTelNum(String telNum) {
        this.telNum = telNum;
    }

    public String getApplChgCodeName() {
        return applChgCodeName;
    }

    public void setApplChgCodeName(String applChgCodeName) {
        this.applChgCodeName = applChgCodeName;
    }

    public String getChgStsCodeName() {
        return chgStsCodeName;
    }

    public void setChgStsCodeName(String chgStsCodeName) {
        this.chgStsCodeName = chgStsCodeName;
    }
}
